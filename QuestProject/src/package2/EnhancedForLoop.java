package package2;

public class EnhancedForLoop {

	public static void main(String[] args) {
		String[] arr1= {"arc","blue","balack"};
		for(String var:arr1) {
			System.out.println(var);
			System.out.println(",");
		}
		int[] arr2= {1,2,3,4};
		for(int var1:arr2) {
			System.out.println(var1);
			System.out.println(",");
		}
	}

}
