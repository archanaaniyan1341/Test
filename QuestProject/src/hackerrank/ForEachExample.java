package hackerrank;


	public class ForEachExample {
		public static void main(String[] args) { 
			float[] myArray = { (float) 12.33, (float) 22.99, (float) 43.44, (float) 53.56 };
			// To display all the contents in array
			for (double contents : myArray) {
				System.out.println(contents);
				}// end of for each loop }// end of main method}// End of ForEachExample
	}
}
