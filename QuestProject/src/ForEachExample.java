
	public class ForEachExample {
		public static void main(String[] args) {
			float[] myArray = { 12.33f, 22.99f, 43.44f, 53.56f }; // To display all the contents in array 
			for (double contents : myArray) { 
				System.out.println(contents); }// end of for each loop
			}// end of main method
		}// End of ForEachExample
		
	

