
public class ClassAssignment {

	public static void main(String[] args) {
		int evenSum=0;
		int oddSum=0;
		for(int i=1;i<=100;i++){
			if(i%2==0) {
				evenSum=evenSum+i;
			}
			else {
				 oddSum=oddSum+i;	
			}
			System.out.println("Even sum is"+evenSum);
			System.out.println("Odd sum is"+oddSum);
			
		}	     

				      
	} 
}				
		

	


